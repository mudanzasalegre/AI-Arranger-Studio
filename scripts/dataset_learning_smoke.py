from __future__ import annotations

import json
import shutil
from pathlib import Path

import mido
from arranger_core import GenerationSpec, export_project, generate_arrangement
from dataset_tools import PatternIndex, create_manifest, import_dataset
from music21 import harmony, meter, note, stream

TICKS_PER_BEAT = 480
HALF_BEATS = tuple(index * 0.5 for index in range(8))


def main() -> None:
    workspace = Path.cwd().resolve()
    outputs_root = (workspace / "outputs").resolve()
    demo_root = (outputs_root / "obj9_dataset_demo").resolve()
    if outputs_root not in demo_root.parents:
        raise RuntimeError(f"Refusing to clean path outside outputs/: {demo_root}")
    if demo_root.exists():
        shutil.rmtree(demo_root)

    source_dir = demo_root / "source"
    import_dir = demo_root / "imported"
    export_dir = demo_root / "generated"
    source_dir.mkdir(parents=True)

    for variant in range(10):
        _write_dataset_midi(source_dir / f"learn_{variant:02d}.mid", variant=variant)
    _write_progression_musicxml(source_dir / "progression.musicxml")

    manifest_path = demo_root / "dataset_manifest.json"
    create_manifest(
        source_dir,
        manifest_path,
        default_metadata=_default_metadata(),
        metadata_by_name={
            "learn_00.mid": {"usable_for_pattern_extraction": False},
            "learn_01.mid": {"quality": 2},
            "learn_08.mid": {"usable_for_training": False},
            "progression.musicxml": {"tags": ["harmony", "progressions"]},
        },
    )
    import_summary = import_dataset(source_dir, manifest_path, import_dir)
    pattern_index = PatternIndex.load_json(import_summary.pattern_index_path)

    project = generate_arrangement(
        GenerationSpec(
            prompt="OBJ9 dataset learning smoke",
            ensemble="jazz_trio",
            form="minor_blues_12",
            style="hard_bop",
            seed=82,
            constraints={"pattern_index_path": import_summary.pattern_index_path},
        ),
        project_id="obj9-dataset-learning-smoke",
    )
    duration_issues = project.validate_bar_durations()
    if duration_issues:
        raise RuntimeError(f"Generated project has duration issues: {duration_issues}")

    export_manifest = export_project(project, export_dir, include_pdf=False)
    summary = {
        "source_midis": len(list(source_dir.glob("*.mid"))),
        "imported_files": import_summary.imported_files,
        "skipped_for_license": import_summary.skipped_for_license,
        "skipped_for_quality": import_summary.skipped_for_quality,
        "extracted_patterns": import_summary.extracted_patterns,
        "pattern_counts": import_summary.pattern_counts,
        "profiled_files": import_summary.profiled_files,
        "role_counts": import_summary.role_counts,
        "pattern_index_path": import_summary.pattern_index_path,
        "profile_report_path": import_summary.profile_report_path,
        "role_manifest_path": import_summary.role_manifest_path,
        "pattern_categories": sorted({pattern.category for pattern in pattern_index.patterns}),
        "pattern_index_used": project.metadata.get("pattern_index_used"),
        "export_dir": str(export_dir),
        "exported_files": len(export_manifest["files"]),
    }
    (demo_root / "smoke_summary.json").write_text(
        json.dumps(summary, indent=2) + "\n",
        encoding="utf-8",
    )
    print(json.dumps(summary, indent=2))


def _default_metadata() -> dict[str, object]:
    return {
        "source": "synthetic_obj9_smoke",
        "license": "CC0-1.0",
        "copyright_notes": "Generated by scripts/dataset_learning_smoke.py",
        "usable_for_training": True,
        "usable_for_pattern_extraction": True,
        "style": "hard_bop",
        "quality": 4,
        "tags": ["drums", "walking_bass", "piano", "melody", "horn_response"],
    }


def _write_dataset_midi(path: Path, *, variant: int) -> None:
    midi_file = mido.MidiFile(type=1, ticks_per_beat=TICKS_PER_BEAT)
    midi_file.tracks.append(_midi_track("Drum Kit", _drum_events(variant)))
    midi_file.tracks.append(_midi_track("Double Bass", _bass_events(variant)))
    midi_file.tracks.append(_midi_track("Piano", _piano_events(variant)))
    midi_file.tracks.append(_midi_track("Alto Sax Lead", _melody_events(variant)))
    midi_file.tracks.append(_midi_track("Trumpet in Bb", _horn_events(variant)))
    midi_file.save(path)


def _drum_events(variant: int) -> list[tuple[int, int, mido.Message]]:
    events: list[tuple[int, int, mido.Message]] = []
    ride_pitch = 51 if variant % 2 == 0 else 42
    for bar in range(4):
        bar_start = bar * 4
        for beat in HALF_BEATS:
            _add_note(events, channel=9, note_number=ride_pitch, start=bar_start + beat)
            if beat in {0.0, 2.0}:
                _add_note(events, channel=9, note_number=36, start=bar_start + beat)
            if beat in {1.5, 3.5}:
                _add_note(events, channel=9, note_number=38, start=bar_start + beat)
            if beat in {1.0, 3.0}:
                _add_note(events, channel=9, note_number=44, start=bar_start + beat)
    return events


def _bass_events(variant: int) -> list[tuple[int, int, mido.Message]]:
    cells = ([0, 3, 7, 10], [0, 4, 7, 9], [0, 2, 5, 11], [0, 5, 7, 10])
    root = 36 + variant
    events: list[tuple[int, int, mido.Message]] = []
    for bar in range(4):
        for beat, interval in enumerate(cells[(variant + bar) % len(cells)]):
            _add_note(
                events,
                channel=0,
                note_number=root + interval + (bar % 2) * 2,
                start=bar * 4 + beat,
                duration=0.9,
                velocity=76,
            )
    return events


def _piano_events(variant: int) -> list[tuple[int, int, mido.Message]]:
    shapes = ([0, 3, 6, 10], [0, 4, 7, 11], [0, 5, 9, 14], [0, 3, 7, 12])
    base = 52 + variant % 5
    events: list[tuple[int, int, mido.Message]] = []
    for bar in range(4):
        for start in (0.5, 1.75, 3.0):
            for interval in shapes[(variant + bar) % len(shapes)]:
                _add_note(
                    events,
                    channel=1,
                    note_number=base + interval,
                    start=bar * 4 + start,
                    duration=0.7,
                    velocity=68,
                )
    return events


def _melody_events(variant: int) -> list[tuple[int, int, mido.Message]]:
    motifs = ([0, 2, 3, 7], [0, -2, 1, 5], [0, 3, 5, 8], [0, 1, -1, 4])
    base = 62 + variant % 6
    events: list[tuple[int, int, mido.Message]] = []
    for bar in range(4):
        for start, interval in zip(
            (0.0, 0.75, 1.5, 2.5),
            motifs[variant % len(motifs)],
            strict=False,
        ):
            _add_note(
                events,
                channel=2,
                note_number=base + interval + bar % 2,
                start=bar * 4 + start,
                duration=0.5,
                velocity=82,
            )
    return events


def _horn_events(variant: int) -> list[tuple[int, int, mido.Message]]:
    events: list[tuple[int, int, mido.Message]] = []
    base = 67 + variant % 4
    for bar in range(4):
        for start, interval in ((2.0, 0), (2.5, 4 + variant % 3)):
            _add_note(
                events,
                channel=3,
                note_number=base + interval + bar % 2,
                start=bar * 4 + start,
                duration=0.5,
                velocity=84,
            )
    return events


def _add_note(
    events: list[tuple[int, int, mido.Message]],
    *,
    channel: int,
    note_number: int,
    start: float,
    duration: float = 0.5,
    velocity: int = 80,
) -> None:
    start_tick = round(start * TICKS_PER_BEAT)
    end_tick = start_tick + round(duration * TICKS_PER_BEAT)
    events.append(
        (
            start_tick,
            1,
            mido.Message("note_on", channel=channel, note=note_number, velocity=velocity),
        )
    )
    events.append(
        (
            end_tick,
            0,
            mido.Message("note_off", channel=channel, note=note_number, velocity=0),
        )
    )


def _midi_track(
    name: str,
    events: list[tuple[int, int, mido.Message]],
) -> mido.MidiTrack:
    track = mido.MidiTrack()
    track.append(mido.MetaMessage("track_name", name=name, time=0))
    previous_tick = 0
    for tick, _, message in sorted(events, key=lambda item: (item[0], item[1])):
        message.time = tick - previous_tick
        track.append(message)
        previous_tick = tick
    return track


def _write_progression_musicxml(path: Path) -> None:
    chords = ["Cm7", "F7", "G7", "C7", "Am7", "D7", "Em7", "A7"]
    score = stream.Score(id="obj9-progression-smoke")
    part = stream.Part(id="harmony")
    for index, symbol in enumerate(chords, start=1):
        measure = stream.Measure(number=index)
        if index == 1:
            measure.insert(0, meter.TimeSignature("4/4"))
        measure.insert(0, harmony.ChordSymbol(symbol))
        measure.append(note.Rest(quarterLength=4.0))
        part.append(measure)
    score.append(part)
    score.write("musicxml", fp=str(path))


if __name__ == "__main__":
    main()
